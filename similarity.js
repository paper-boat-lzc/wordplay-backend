// 4. 编辑距离计算（字形相似度）
// ============================================
function levenshteinDistance(a, b) {
  const matrix = [];
  for (let i = 0; i <= b.length; i++) {
    matrix[i] = [i];
  }
  for (let j = 0; j <= a.length; j++) {
    matrix[0][j] = j;
  }
  for (let i = 1; i <= b.length; i++) {
    for (let j = 1; j <= a.length; j++) {
      if (b.charAt(i - 1) === a.charAt(j - 1)) {
        matrix[i][j] = matrix[i - 1][j - 1];
      } else {
        matrix[i][j] = Math.min(
          matrix[i - 1][j - 1] + 1,
          matrix[i][j - 1] + 1,
          matrix[i - 1][j] + 1
        );
      }
    }
  }
  return matrix[b.length][a.length];
}

// ============================================
// 5. 核心相似度计算函数
// ============================================
function calculateSimilarity(guess, answer) {
  // 预处理
  const guessNorm = guess.trim();
  const answerNorm = answer.trim();
  
  let finalScore = 0;
  let reason = '';
  let factors = [];

  // ======================================
  // 层级1：精确匹配（100分）
  // ======================================
  if (guessNorm === answerNorm) {
    return { 
      score: 100, 
      isCorrect: true, 
      reason: '🎉 完全正确！',
      factors: ['精确匹配']
    };
  }

  // ======================================
  // 层级2：同义词匹配（90-100分）
  // ======================================
  const synonyms = synonymMap[answerNorm] || [];
  if (synonyms.includes(guessNorm)) {
    return { 
      score: 95, 
      isCorrect: true, 
      reason: '✨ 同义词匹配！',
      factors: ['同义词']
    };
  }
  
  // 反向同义词检查（猜词是主词，答案是同义词）
  for (const [mainWord, syns] of Object.entries(synonymMap)) {
    if (syns.includes(answerNorm) && mainWord === guessNorm) {
      return { 
        score: 95, 
        isCorrect: true, 
        reason: '✨ 同义词匹配！',
        factors: ['同义词']
      };
    }
  }

  // ======================================
  // 层级3：包含关系（75-90分）
  // ======================================
  if (guessNorm.includes(answerNorm)) {
    finalScore += 85;
    factors.push('包含答案');
    reason = '包含答案关键词！';
  } else if (answerNorm.includes(guessNorm) && guessNorm.length >= 2) {
    finalScore += 75;
    factors.push('部分匹配');
    reason = '部分匹配关键词！';
  }

  // ======================================
  // 层级4：语义关联网络（20-80分）
  // ======================================
  if (semanticNetwork[answerNorm] && semanticNetwork[answerNorm][guessNorm]) {
    const semanticScore = semanticNetwork[answerNorm][guessNorm];
    if (semanticScore > finalScore) {
      finalScore = semanticScore;
    }
    factors.push('语义关联');
    if (semanticScore >= 80) reason = '非常接近！';
    else if (semanticScore >= 60) reason = '很接近了！';
    else if (semanticScore >= 40) reason = '有点关联！';
    else if (semanticScore >= 20) reason = '勉强相关';
  }

  // ======================================
  // 层级5：同类别匹配（60-70分）
  // ======================================
  const answerCategory = wordToCategory[answerNorm];
  const guessCategory = wordToCategory[guessNorm];
  
  if (answerCategory && guessCategory && answerCategory === guessCategory) {
    const categoryScore = 65;
    if (categoryScore > finalScore) {
      finalScore = categoryScore;
    }
    factors.push('同类别');
    if (!reason) reason = '同属一类！';
  }

  // ======================================
  // 层级6：编辑距离（字形相似，10-50分）
  // ======================================
  const maxLen = Math.max(guessNorm.length, answerNorm.length);
  const distance = levenshteinDistance(guessNorm, answerNorm);
  const editScore = Math.round((1 - distance / maxLen) * 50);
  
  if (editScore > 10 && editScore > finalScore * 0.5) {
    finalScore = Math.max(finalScore, editScore);
    factors.push('字形相似');
  }

  // ======================================
  // 层级7：基础保底分（避免0分）
  // ======================================
  if (finalScore === 0) {
    // 单字给5分保底
    if (guessNorm.length === 1 || answerNorm.length === 1) {
      finalScore = 5;
      reason = '微相关';
    } else {
      // 完全无关给0-5分随机
      finalScore = Math.floor(Math.random() * 5);
      reason = '不太相关';
    }
  }

  // ======================================
  // 最终分数校准
  // ======================================
  finalScore = Math.min(99, Math.max(0, finalScore));
  finalScore = Math.round(finalScore);

  // 根据最终分数确定原因
  if (!reason) {
    if (finalScore >= 80) reason = '非常接近！';
    else if (finalScore >= 60) reason = '很接近了！';
    else if (finalScore >= 40) reason = '有点关联！';
    else if (finalScore >= 20) reason = '勉强相关';
    else if (finalScore >= 10) reason = '微相关';
    else reason = '不太相关';
  }

  return {
    score: finalScore,
    isCorrect: finalScore >= 85,
    reason: reason,
    factors: factors
  };
}

// ============================================
// 6. 批量计算
// ============================================
function batchCalculate(guesses, answer) {
  return guesses.map(guess => ({
    ...guess,
    similarity: calculateSimilarity(guess.text, answer)
  }));
}

